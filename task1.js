var a = 10;
var b = "10";

console.log(a);
console.log(b);

// Equality
console.log("a == b  :", a == b);   
console.log("a === b :", a === b);  

// Inequality
console.log("a != b  :", a != b);   
console.log("a !== b :", a !== b);  

// Relational operators
console.log("a > b   :", a > b);    
console.log("a < b   :", a < b);    
console.log("a >= b  :", a >= b);   
console.log("a <= b  :", a <= b);   
